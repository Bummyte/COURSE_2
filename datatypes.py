#for integral
A = 4 
print (A)
print (type(A))

#for string datatype
B = "Hello World"
print (B)
print (type(B))

#for float datatype
C = 15.5
print (C)
print (type(C))

#for complex datatype
D = 2j 
type_D = type(D)
print(f"the value of D is {D}, and the data type of D is {type_D}")

#list datatype
X = ["Love", "Peace", "Joy"]
type_X = type(X)
print(f"the value of x is {X},and the datatype is {type_X}")

#for range datatype
Y = range(5)
type_Y = type(Y)
print(f"the value of Y is {Y}, and the datatype is {type_Y}")






